#Comparisons:
#Equal: 3==2 
#Not Equal: 3!=2 
#Greater Than: 3>2 
#Less Than: 3<2 
#Greater or Equal: 3>=2 
#Less or Equal: 3<=2 

num_1 = 3 
num_2 = 2 

#note if the below statement is True is going to return True if False is going to Return False 
print(num_1 == num_2)
print(num_1 != num_2)
print(num_1 > num_2)
print(num_1 < num_2)
print(num_1 >= num_2)
print(num_1 <= num_2)

